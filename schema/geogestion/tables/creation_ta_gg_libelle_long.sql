/*
TA_GG_LIBELLE_LONG: Contient les libelles longs qui caractérises les éléments contenus dans le schéma G_GESTIONGEO.
*/

-- 1. TA_GG_LIBELLE_LONG
-- 1.1. Création de la table
CREATE TABLE G_GESTIONGEO.TA_GG_LIBELLE_LONG(
    objectid NUMBER(38,0) GENERATED BY DEFAULT AS IDENTITY,
    valeur VARCHAR2(255)
);

