-- 1.3. Creation de la table TA_PTTOPO_INTEGRATION_3D

-- 1.3.1. Creation de la table
CREATE TABLE G_GESTIONGEO.TA_PTTOPO_INTEGRATION_3D
	(
	OBJECTID NUMBER(38,0) GENERATED BY DEFAULT AS IDENTITY START WITH 1 INCREMENT BY 1,
	GEOM SDO_GEOMETRY NOT NULL,
	FID_NUMERO_DOSSIER NUMBER(38,0) NOT NULL,
	ALT NUMBER(6,3),
	MAT NUMBER(38,0)
	)
;

-- 1.3.2. Commentaire de la table

COMMENT ON TABLE TA_PTTOPO_INTEGRATION_3D IS 'Table qui contient les points topographique releves par les geometre';

-- 1.3.3. Commentaires des colonnes
COMMENT ON COLUMN G_GESTIONGEO.TA_PTTOPO_INTEGRATION_3D.OBJECTID IS 'Cle primaire de la table';
COMMENT ON COLUMN G_GESTIONGEO.TA_PTTOPO_INTEGRATION_3D.GEOM IS 'Geometrie de l''element - type point';
COMMENT ON COLUMN G_GESTIONGEO.TA_PTTOPO_INTEGRATION_3D.FID_NUMERO_DOSSIER IS 'Clé étrangère vers la table TA_GG_DOSSIER. Numero du dossier pour lequel le point a été relevé.';
COMMENT ON COLUMN G_GESTIONGEO.TA_PTTOPO_INTEGRATION_3D.ALT IS 'Altitude du point';
COMMENT ON COLUMN G_GESTIONGEO.TA_PTTOPO_INTEGRATION_3D.MAT IS 'Matricule du point';


-- 1.3.4. Contraintes
-- 1.3.4.1 Clé primaire

ALTER TABLE G_GESTIONGEO.TA_PTTOPO_INTEGRATION_3D
ADD CONSTRAINT TA_PTTOPO_INTEGRATION_3D_PK 
PRIMARY KEY (OBJECTID)
USING INDEX TABLESPACE "G_ADT_INDX";

-- 1.3.4.2 Clé étrangères

-- 1.3.4.2.1. CHAMP FID_NUMERO_DOSSIER vers G_GESTIONGEO.TA_GG_DOSSIER
ALTER TABLE G_GESTIONGEO.TA_PTTOPO_INTEGRATION_3D
ADD CONSTRAINT TA_PTTOPO_INTEGRATION_3D_FID_NUMERO_DOSSIER_FK 
FOREIGN KEY (FID_NUMERO_DOSSIER)
REFERENCES G_GESTIONGEO.TA_GG_DOSSIER(objectid);


