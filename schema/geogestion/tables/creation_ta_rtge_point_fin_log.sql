--	DATE_FIN_VALIDITE DATE,
	TEXTE VARCHAR2(2048 BYTE),
	LONGUEUR NUMBER(8,0),
	LARGEUR NUMBER(8,0),
	ORIENTATION NUMBER(5,2),
	HAUTEUR NUMBER(8,0),
	INCLINAISON NUMBER(5,2),
--	GEO_TYPE CHAR(1 BYTE),
	FID_PNOM_MODIFICATION NUMBER(38,0),
	DATE_MODIFICATION DATE,
	MODIFICATION NUMBER(38,0)
	)
