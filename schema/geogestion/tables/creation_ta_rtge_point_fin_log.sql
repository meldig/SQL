-- 3.1.1. Creation de la table
CREATE TABLE G_GESTIONGEO.TA_RTGE_POINT_FIN_LOG
	(
	OBJECTID NUMBER(38,0),
	GEOM SDO_GEOMETRY,
	FID_IDENTIFIANT NUMBER(38,0),
	NUMERO_DOSSIER VARCHAR2(13 BYTE),	
	IDENTIFIANT_TYPE NUMBER(8,0),
--	GEO_INSEE CHAR(3 BYTE),
